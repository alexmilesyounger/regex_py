# \w - matches any Unicode word character
# \W - matches anything that isn't a Unicode word character
# \s - matches any whitespace
# \S - matches anything that isn't whitespace
# \d - matches any number from 0 to 9
# \D - matches anything that isn't a number
# \b - matches word boundaries or edges of a word
# \B - matches anything that isn't the edge of a word